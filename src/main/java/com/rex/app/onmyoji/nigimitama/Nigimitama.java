package com.rex.app.onmyoji.nigimitama;

public class Nigimitama {
	private String seqNo;
	private String type;
	private String grade;
	private String starlevel;
	private String position;
	private NgAbAddition ngAbAddition;
	
	public String getSeqNo() {
		return seqNo;
	}
	public void setSeqNo(String seqNo) {
		this.seqNo = seqNo;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getStarlevel() {
		return starlevel;
	}
	public void setStarlevel(String starlevel) {
		this.starlevel = starlevel;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public NgAbAddition getNgAbAddition() {
		return ngAbAddition;
	}
	public void setNgAbAddition(NgAbAddition ngAbAddition) {
		this.ngAbAddition = ngAbAddition;
	}

}
